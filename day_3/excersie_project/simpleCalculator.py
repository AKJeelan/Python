
# Build a simple calculator that takes two numbers and prints results for all 5 basic operations (+, -, *, /, %).

print('Welcome to Simple Calculator')
print('------------------------')

num1 = input('Enter 1st Number : ')
num2 = input('Enter 2nd Number : ')

num1 = float(num1)
num2 = float(num2)

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)